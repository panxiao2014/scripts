%% =================== Part 3: Advanced optimization ===================
fprintf('Running Advanced optimization ...\n')

global X = [ones(m, 1), data(:,1)]; % Add a column of ones to x

% Some optimization settings
iterations = 3000;

function [jVal, gradient] = costFunction(theta)
  global m;
  global X;
  global y;
  jVal = 1/(2*m) * sum((X*theta - y).^2);
  gradient = 1/m * (X' * (X * theta - y));
end

options = optimset('GradObj', 'on', 'MaxIter', iterations);
initTheta = zeros(2, 1);
[theta, functionVal, exitFlag] = fminunc(@costFunction, initTheta, options);

% print theta to screen
fprintf('Theta found by gradient descent: ');
fprintf('%f %f \n', theta(1), theta(2));

% Plot the linear fit
hold on; % keep previous plot visible
plot(X(:,2), X*theta, '-')
legend('Training data', 'Linear regression')
